// Using autoboxing and unboxing
public class AutoBox {
	public static void main(String args[]) {
		
		// autoboxing and unboxing values 100
		Integer iOb = 100;
		int i = iOb;
		
		System.out.println(i + " " + iOb); // displays 100 100
	}
}
